// Write a program to find addition,subtraction,multiplication,division of two numbers.
#include <iostream>
using namespace std;
int main() {
    double a,b;

    cout<< "Enter 2 Numbers:";
    cin>> a>>b;
    cout<<"\nAddition:"<<(a+b);
    cout<<"\nSubtraction:"<<(a-b);
    cout<<"\nMultiplication:"<<(a*b);
    cout<<"\nDivision:"<<(a/b);
    return 0;  

}